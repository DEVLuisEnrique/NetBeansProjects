    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        String db = "";
        try {
           Statement sql = Conexion.getConnection().createStatement();
           String consulta = "SELECT LastName FROM Northwind.dbo.Employees";
                  // + "WHERE LastName LIKE 'd%'";
           
           ResultSet resultado = sql.executeQuery(consulta);
           
           while(resultado.next())
            {
               db += resultado.getString(1)+ "\n";
            }
           JOptionPane.showMessageDialog(null, db);
           
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error de consulta: "+e);
        }
        
    }//GEN-LAST:event_btnConectarActionPerformed

   
